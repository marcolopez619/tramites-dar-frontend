
/**
 * Modelo de error.
 *
 * @export
 * @class Error
 */
export class Error {

    /**
     * Código del error.
     *
     * @type {string}
     * @memberof Error
     */
    public code: string;

    /**
     * Descripción del error.
     *
     * @type {string}
     * @memberof Error
     */
    public description: string;
}
